// patterns.test.ts
//
// Unit tests that pin the regex behaviour to the actual leak history.
//
// Two invariants:
//   1. Every known-bad fixture (the 4 leaked messages from 2026-06-04/05) MUST
//      be caught by every pattern id listed in its `must_fire`. If a future
//      "simplification" of patterns.ts weakens one of these, this test fails
//      and CI blocks the change.
//   2. Documented carve-outs (fiscal Q1-Q4, "teaching assistant") MUST NOT
//      false-fire. SPEC §4.5 and §7.2 explicitly accept these as tradeoffs.

import { describe, it, expect } from "vitest";
import { PATTERNS, compile, type ForbiddenPattern } from "./patterns";
import {
  LEAKED_FIXTURES,
  FISCAL_QUARTER_PASSES,
  FIRST_PERSON_PASSES,
} from "./fixtures/leaked-messages";

function patternsById(): Map<string, ForbiddenPattern> {
  const map = new Map<string, ForbiddenPattern>();
  for (const p of PATTERNS) map.set(p.id, p);
  return map;
}

describe("patterns.ts: known-bad fixtures fire as required", () => {
  const byId = patternsById();

  for (const fx of LEAKED_FIXTURES) {
    for (const patternId of fx.must_fire) {
      it(`fixture ${fx.id} fires pattern ${patternId}`, () => {
        const p = byId.get(patternId);
        expect(p, `pattern ${patternId} not in patterns.ts`).toBeDefined();
        const { re, allow } = compile(p!);
        if (allow && allow.test(fx.body)) {
          throw new Error(
            `pattern ${patternId} allowlist matched the known-bad fixture ${fx.id} — allowlist is too broad`,
          );
        }
        const hit = re.test(fx.body);
        expect(
          hit,
          `pattern ${patternId} (${p!.label}) did NOT match known-bad body of ${fx.id}: "${fx.body}"`,
        ).toBe(true);
      });
    }
  }

  it("at least one CRITICAL or HIGH pattern fires on every known-bad fixture", () => {
    const heavy = PATTERNS.filter(
      (p) => p.severity === "critical" || p.severity === "high",
    ).map((p) => ({ p, ...compile(p) }));

    for (const fx of LEAKED_FIXTURES) {
      const fired = heavy.some(({ allow, re }) => {
        if (allow && allow.test(fx.body)) return false;
        return re.test(fx.body);
      });
      expect(
        fired,
        `no CRITICAL/HIGH pattern fires on ${fx.id} — that is a SPEC §7.1 hole`,
      ).toBe(true);
    }
  });
});

describe("patterns.ts: documented carve-outs do not false-fire", () => {
  const byId = patternsById();
  const qPriority = byId.get("framework.q-priority-code");
  const persona = byId.get("voice.persona-break");

  it("framework.q-priority-code passes fiscal-quarter usage (SPEC §4.5)", () => {
    expect(qPriority).toBeDefined();
    const { re } = compile(qPriority!);
    for (const fx of FISCAL_QUARTER_PASSES) {
      expect(
        re.test(fx.body),
        `priority-code regex false-fired on fiscal-quarter body ${fx.id}: "${fx.body}"`,
      ).toBe(false);
    }
  });

  it("voice.persona-break passes 'teaching assistant' / 'assistant principal' (SPEC §7.2)", () => {
    expect(persona).toBeDefined();
    const { re } = compile(persona!);
    for (const fx of FIRST_PERSON_PASSES) {
      expect(
        re.test(fx.body),
        `persona regex false-fired on non-first-person body ${fx.id}: "${fx.body}"`,
      ).toBe(false);
    }
  });

  it("style.dash.banned catches U+2014 / U+2013 / U+2015 / -- (SPEC §7.2)", () => {
    const p = byId.get("style.dash.banned");
    expect(p).toBeDefined();
    const { re } = compile(p!);
    const seeds = ["a — b", "a – b", "a ― b", "a -- b"];
    for (const s of seeds) {
      expect(re.test(s), `dash regex missed seed: "${s}"`).toBe(true);
    }
    // And a comma-spaced sentence break (the SPEC-approved style) passes.
    expect(re.test("a, b")).toBe(false);
  });

  it("leak.stack.model catches 'ChatGPT' despite \\b gap (SPEC §7.2)", () => {
    const p = byId.get("leak.stack.model");
    expect(p).toBeDefined();
    const { re } = compile(p!);
    expect(re.test("It was generated by ChatGPT.")).toBe(true);
    expect(re.test("It was generated by Chat GPT.")).toBe(true);
    expect(re.test("It was generated by GPT.")).toBe(true);
  });

  it("leak.owner_private.token fires on storage-code surface", () => {
    const p = byId.get("leak.owner_private.token");
    expect(p).toBeDefined();
    const { re } = compile(p!);
    expect(re.test("kind=owner_private was filtered")).toBe(true);
    expect(re.test("This is owner_private content.")).toBe(true);
  });
});
